					<h1>Our Story</h1>
						
						<p>Spring Hill Senior Living was born of personal experiences.  Wayne Heikkala and Andrew Plant were touched by circumstances with aging family members.  Roger Wendel, D.M.D was moved by the stories of his patients and Michael O'Rourke was stirred by the residents he knew living in senior housing communities.  In each of their lives they saw how a positive social environment, purpose and social connection contributed to quality of life for seniors. </p>  

<p>In 2000 they shared their experiences and beliefs that senior housing could be more than buildings; it could be services, programs, support - engaging residents and families in creating rich living experiences.  They wanted to make a difference.  Their difference would be Spring Hill Senior Living.</p>

<p><b>Wayne Heikkala</b> is co-founder and board member.  His professional background is in banking, general contracting and development.  He has been constructing seniors housing since 1986.  Wayne inspires Spring Hill's commitment to providing the best opportunity for residents to live a carefree lifestyle.</p>

<p><b>Andrew Plant</b> is a co-founder and board member.  His professional experience is in real estate and general contracting.  He constructed his first seniors housing community in 1996.  Andy is responsible for formulating and implementing Spring Hill's strategic growth plan.  He believes the benefits of health and wellness; life long learning and active life are incalculable for every resident.</p>

<p><b>Roger Wendel, D.M.D.</b> is an active member and on the board and founder/principal owner of Wendel Family Dental Centers.  He is the champion of respect for each resident and employee.</p>

<p><b>Michael O'Rourke</b> is an active member and on the board.  His entire career has been devoted to the seniors housing industry.  He is an owner of facilities and has participated in the development of seven senior communities.  Michael's commitment is ensuring Spring Hill Senior Living is an industry leader and innovator.
</p>

<p>Today along with Chief Executive Officer, Ronnie Cook and nearly 300 employees they work to create physical environments that are warm and comfortable; services tailored to the preferences of each resident; programs focused on health and wellness; opportunities to learn and grow and social connection - <b>Better Living</b>.
