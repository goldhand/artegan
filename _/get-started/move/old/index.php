<h1><a name="to-move">To Move or Not To Move: That is the Question</a></h1>

<p>Time and circumstances sneak up on us.  Recently you might have realized that staying in your home is becoming increasing troublesome.</p>


<dl>
<dt>Perhaps you've thought:</dt>

<dd>
<ul>
	<li>Navigating stairs is becoming a challenge.</li>
	<li>Home maintenance is no longer manageable.</li>
	<li>Even with family and friends nearby most of my time is spent alone.</li>
	<li>I don't exercise regularly.</li>
	<li>Sometimes I forget to eat meals.</li>
	<li>Cooking for one is just too much trouble.</li>
	<li>Sometimes I forget to take my medication.</li>
	<li>I no longer have hobbies or regular activities.</li>
	<li>I don't drive any more and I don't like asking others for transportation.</li>
	<li>If there were a fire or other emergency I don't have an escape plan I could manage on my own.</li>
	<li>I'm not comfortable hiring strangers to come into my home to provide services.</li>
	<li>I'm no longer able to care for my spouse and myself alone.</li>
</ul>

</dd>
</dl>


<p>Any or all of these thoughts may mean it's time to begin a conversation about different living options.</p>

<p>Where do you want to live?  How do you want to live?  What can you manage?  What do you need help with?  You'll find our <a class="dk_blue" href="http://www.springhillseniorliving.com/communities/living-options/decision-quiz.php">Senior Living Options Decision Quiz</a> on our web site easy to navigate and helpful in considering which option might best meet your needs.</p>

<h1><a name="help">Help For Older Adults Who Are Planning to Relocate</a></h1>
Any move is very stressful.  And moving from your long-time residence is even more so.   An experienced Senior Move Manager can help reduce the stress by helping you through each step to a successful relocation.</p>

<p><b><a name="manager">What Is A Senior Move Manager?</a></b><br>
A Senior Move Manager is a professional who provides emotional, organizational, and hands-on support to older adults and their families during the move process.  He/she understands the difficult experience of moving from a long-time residence and makes that move as stress-free as possible.</p>

<dl>
<dt><b><a name="manager-does">What Does A Senior Move Manager Do?</a></b></dt>

<dd>
<ul>
	<li>Set up a time-line and comprehensive plan for your move</li>
	<li>Locate and oversee a mover</li>
	<li>Draw up a scale-model floor plan of your new residence</li>
	<li>Create scale-models of your furniture to help you plan your space</li>
	<li>Help you stay focused on the task at hand</li>
	<li>Help you sort through your possessions</li>
	<li>Pack those items to be moved</li>
	<li>Arrange for the disposal of unwanted items (donations, estate sales, etc.)</li>
	<li>Unpack and organize your new home</li>
	<li>Help you prepare your home for sale</li>
</ul>
</dd>
</dl>


<p><b><a name="find-manager">How Do I Find A Senior Move Manager?</a></b><br>
The National Association of Senior Move Managers (<a class="dk_blue" target="blank" href="http://www.nasmm.com">www.nasmm.com</a>) provides a directory of Senor Move Managers in your locale.</p> 

<dl>
<dt><b><a name="questions">What Questions Do I Ask?</a></b></dt>
<dt>As you interview potential senior move managers, keep in mind some pertinent questions:</dt>
<dd>
<ul>
	<li>How long have you been a Senior Move Manager?</li>
	<li>Are you insured and licensed by your state?</li>
	<li>Do you have letters of reference/phone numbers I may call?</li>
	<li>What is your fee structure?</li>
	<li>Will you provide me with a written contract?</li>
	<li>What services do you provide yourself and which ones are provided by others?</li>
</ul>
</dd></dl>

<p>Even if you have family and friends who are willing to help, a Senior Move Manager has expertise and experience that will provide you with economical and insightful solutions as well as connections to local resources to expedite your move.</p>

<p>When in doubt the Spring Hill Senior Living executive directors and community relations staff are ready to listen and offer guidance to help you make the most informed decision.</p>


						
