<p><b>Communicating</b><br>
Is communicating with your loved one more and more challenging?</p>


<p><b>Behavior</b><br>
Have you had to change your home's physical environment to accommodate your loved one's behavior?</p>

<p><b>Wandering</b><br>
Is your loved one wandering?</p>

