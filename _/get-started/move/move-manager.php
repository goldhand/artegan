<h1>Help for Older Adults Who are Planning to Relocate</h1>

<p>Any move is stressful and moving from your long-time residence is even more so.  Experienced Senior Move Managers can help reduce that stress by helping you with each step to a successful relocation.</p>

<h2>What Are Senior Move Managers?</h2>
<p>Senior Move Managers are professionals who provide emotional, organizational, and hands-on support to older adults and their families during the move process.</p>

<dl>
<dt><h2>What Do Senior Move Managers Do?</h2></dt>

<dd>
<ul>
	<li>Set up a time-line and comprehensive plan for your move</li>
	<li>Locate and oversee a mover</li>
	<li>Draw up a scale-model floor plan of your new residence</li>
	<li>Create scale-models of your furniture to help you plan your space</li>
	<li>Help you stay focused on the task at hand</li>
	<li>Help you sort through your possessions</li>
	<li>Pack those items to be moved</li>
	<li>Arrange for the disposal of unwanted items (donations, estate sales, etc.)</li>
	<li>Unpack and organize your new home</li>
	<li>Help you prepare your home for sale</li>
</ul>
</dd>
</dl>


<h2>How Do I Find A Senior Move Manager?</h2>
The National Association of Senior Move Managers (<a class="dk_blue" target="blank" href="http://www.nasmm.com">www.nasmm.com</a>) provides a directory of Senior Move Managers near you.</p> 

<dl>
<dt><h2>What Questions Do I Ask?</h2></dt>
<dt>As you interview potential senior move manages, consider these important questions:</dt>
<dd>
<ul>
	<li>How long have you been a Senior Move Manager?</li>
	<li>Are you insured and licensed by your state?</li>
	<li>Do you have letters of reference/phone numbers I may call?</li>
	<li>What is your fee structure?</li>
	<li>Will you provide me with a written contract?</li>
	<li>What services do you provide yourself and which ones are provided by others?</li>
</ul>
</dd></dl>

<p>Even if you have family and friends who are willing to help, a Senior Move Manager has expertise and experience that will provide you with economical and insightful solutions as well as connections to local resources to expedite your move.</p>

<p>When in doubt the Art&#275;gan community executive directors and community outreach staff are ready to listen and offer guidance to help you make the most informed decision. <a class="dk_blue" href="http://www.springhillseniorliving.com/contact.php">Contact us</a>.</p>


						


						
