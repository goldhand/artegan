					<h1>About Us</h1>

						<h2>The Idea</h2>
						<p>How did Art&#275;gan begin? Where? How? Why? Who?  For many companies the cliché beginning finds a couple of friends sitting in a restaurant and an idea gets scribbled on a napkin. Believe it or not the Art&#275;gan idea was born in a restaurant in Auburn, Alabama nearly 20 years ago when Ronnie Cook and Warren Page shared their mutual desire to do something good in the seniors industry.</p>

						<p>The idea conversation happened when both were working for the same senior living organization; Ronnie as Regional Director and Warren as South East Director of Quality Assurance.  They shared responsibility for a community in Auburn, Alabama.  During a normal monthly community visit their schedules overlapped and at dinner one evening the conversation turned to creating their own senior living company with the goal of being different.  What did different mean?  They weren't sure but they wanted their shared vision conversation to continue in some shape or form.  </p>
						
						
						
						<h2>The Experience</h2>
						<p>Ronnie and Warren continued to work in the seniors industry, eventually winding up at separate organizations.   They enthusiastically gained experience at every level and earned more leadership responsibility.  In 2006 Ronnie was the Executive Vice President for Southern Assisted Living in Chapel Hill, NC and Warren was the Vice President of Operations for South Carolina Lutheran Homes.  They were leading their companies, making a difference on a much larger scale. Southern Assisted Living and South Carolina Lutheran Homes were both very reputable providers.  For Ronnie and Warren their work had meaning and both companies were successful.</p>

						<p>In early 2006 Southern Assisted Living seized the opportunity to merge with a much larger national provider.  Such an alignment comes along only once in a lifetime and it turned out to be a great move for many people. </p>
						
						
						
						<h2>The Move</h2>
						<p>Shortly after the Southern Assisted Living change, Ronnie received a call from a recruiter who was hired by a company based out of Vancouver Washington. The search was on for a CEO to lead a senior living organization. Ronnie accepted the position and moved his family across country. Six months after accepting the CEO position with Spring Hill Senior Living, Ronnie knew he needed a great COO. He called Warren at South Carolina Lutheran Homes. After some quick negotiations, Warren sold his house and moved his family to Portland Oregon and in December of 2006 joined Spring Hill Senior Living as its COO. </p>

						
						
						<h2>The Owners</h2>
						<p>Was the original idea now a reality?  At first glance, yes; Ronnie as CEO and Warren as COO, sharing their vision at Spring Hill Senior Living.  In 2009 Ronnie and Warren saw an opportunity to buy out two owners of Spring Hill Senior Living and finally become OWNERS themselves. Once the deal was done they realized their first idea conversation had in fact become a reality.</p>

						<p>Their initial task was to rename the company based on their shared vision and values.  Art&#275;gan would be a company of value and would embrace a behavior not a culture. Art&#275;gan's mission statement is simple "Celebrating the Art of Life."  They were ready to live the mission and so were their employees, residents and families. </p>
						
						
						
						
						
						<h2>The Name</h2>
						<p>Where did the Art&#275;gan name came from?  Strangely enough it came from a bottle of wine. One day casually reading through a booklet of wine descriptions they spotted the word "artegan". Both liked the sound of the word and were curious about its meaning.  "Artegan" is an Italian derivative that is used to describe architecture. It basically means if you design a home to mirror the town in which it is located (features, color, style etc.) you have created an artegan home.</p>

						<p>What a perfect name for a company that provides employment and service in each community in which it operates.  Art&#275;gan teams work hard to be a part of the fabric of and reflect the values of their home communities. </p>
						
						
						
						<h2>The Future</h2>
						<p>So, Art&#275;gan... Celebrating the Art of Life. Simple really. Life is worth celebrating at any age. Warren and Ronnie continue to build Art&#275;gan around the people they serve and the people who provide the service. </p>
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
